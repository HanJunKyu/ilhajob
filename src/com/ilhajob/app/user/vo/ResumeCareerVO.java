package com.ilhajob.app.user.vo;

public class ResumeCareerVO {
	
	private int num;
	private String id;
	private String career_name;
	private String career_start_date;
	private String career_end_date;
	private String career_rank;
	private String career_position;
	private String career_detail;
	
	public ResumeCareerVO() {;}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCareer_name() {
		return career_name;
	}

	public void setCareer_name(String career_name) {
		this.career_name = career_name;
	}

	

	public String getCareer_start_date() {
		return career_start_date;
	}

	public void setCareer_start_date(String career_start_date) {
		this.career_start_date = career_start_date;
	}

	public String getCareer_end_date() {
		return career_end_date;
	}

	public void setCareer_end_date(String career_end_date) {
		this.career_end_date = career_end_date;
	}

	public String getCareer_rank() {
		return career_rank;
	}

	public void setCareer_rank(String career_rank) {
		this.career_rank = career_rank;
	}

	public String getCareer_position() {
		return career_position;
	}

	public void setCareer_position(String career_position) {
		this.career_position = career_position;
	}

	public String getCareer_detail() {
		return career_detail;
	}

	public void setCareer_detail(String career_detail) {
		this.career_detail = career_detail;
	}

	

}
