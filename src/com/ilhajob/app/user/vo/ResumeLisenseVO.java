package com.ilhajob.app.user.vo;

public class ResumeLisenseVO {
	
	private int num;
	private String id;
	private String lisense_name;
	private String lisense_type;
	private String lisense_publisher;
	private String lisense_date;
	private String lisense_result;
	
	public ResumeLisenseVO() {;}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLisense_name() {
		return lisense_name;
	}

	public void setLisense_name(String lisense_name) {
		this.lisense_name = lisense_name;
	}

	public String getLisense_type() {
		return lisense_type;
	}

	public void setLisense_type(String lisense_type) {
		this.lisense_type = lisense_type;
	}

	public String getLisense_publisher() {
		return lisense_publisher;
	}

	public void setLisense_publisher(String lisense_publisher) {
		this.lisense_publisher = lisense_publisher;
	}

	public String getLisense_date() {
		return lisense_date;
	}

	public void setLisense_date(String lisense_date) {
		this.lisense_date = lisense_date;
	}

	public String getLisense_result() {
		return lisense_result;
	}

	public void setLisense_result(String lisense_result) {
		this.lisense_result = lisense_result;
	}
	
	

}
