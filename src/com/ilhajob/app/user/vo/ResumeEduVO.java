package com.ilhajob.app.user.vo;

public class ResumeEduVO {
	
	private int num;
	private String id;
	private String edu_name;
	private String edu_status;
	private String edu_start_date;
	private String edu_end_date;
	private String edu_major;
	private String edu_grade;
	
	public ResumeEduVO() {;}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEdu_name() {
		return edu_name;
	}

	public void setEdu_name(String edu_name) {
		this.edu_name = edu_name;
	}

	public String getEdu_status() {
		return edu_status;
	}

	public void setEdu_status(String edu_status) {
		this.edu_status = edu_status;
	}

	

	public String getEdu_start_date() {
		return edu_start_date;
	}

	public void setEdu_start_date(String edu_start_date) {
		this.edu_start_date = edu_start_date;
	}

	public String getEdu_end_date() {
		return edu_end_date;
	}

	public void setEdu_end_date(String edu_end_date) {
		this.edu_end_date = edu_end_date;
	}

	public String getEdu_major() {
		return edu_major;
	}

	public void setEdu_major(String edu_major) {
		this.edu_major = edu_major;
	}

	public String getEdu_grade() {
		return edu_grade;
	}

	public void setEdu_grade(String edu_grade) {
		this.edu_grade = edu_grade;
	}
	
	

}
